#include "libft.h"

void	format_symaddr(char dst[], uint64_t addr)
{
	static const char	base[] = "0123456789abcdef";
	uint32_t			i;

	ft_strcpy(dst, "0000000000000000");
	i = 16;
	while (--i && addr)
	{
		dst[i] = base[addr % 16];
		addr /= 16;
	}
}
